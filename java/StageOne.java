    public void makeBG(){
        
               
        for(int i = 0;i < bg.length;i++){
            
            for(int j = 0; j < 4; j++){
                
                if ((i > 1 && i < 3 || i > 8) && j > 2) continue;
                
                else if (i > 12 && j > 1) continue;
                    
                else if(i >= 4 && i <= 9)
                    bg[i] = new BackgroundTwo();
                else 
                    bg[i] = new BackgroundOne();

                addObject(bg[i],(1500) * i,(900-40) * j);
            }
   
        }
  
    }
    
